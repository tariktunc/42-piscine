#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_bom2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= '98')
	{
		b = a + 0;
		while (b <= '99')
		{
			write(1, &a, 1);
			write(1, &b, 1);
			if(a != '98' || b != '99')
				write(1, ", ", 1);
			b++;
		}
		a++;
	}
}

