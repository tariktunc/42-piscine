int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z') || (str[i] >= 97 && str[i] <= 122))
			i++;
		else if (str[i] == ' ')
			i++;
		else
			return (0);
	}
	return (1);
}
