void ft_swap(int *a, int *b)
{
    int swap = *a; 
    *a = *b;
    *b = swap;
}

#include <stdio.h>

int main()
{
    int x;
    int y;
    x = 3;
    y = 5;

    ft_swap(&x, &y);
    printf("%d %d", x, y);
}